function assertOrVerifyFailure(line, isAssert) {
  var message = '"expected failure"';
  var failStatement = isAssert ? "[NUnit.Framework.Assert]::Fail(" + message + ")" :
      "$verificationErrors.Append(" + message + ")";
  return "try\n" +
      "{\n" +
      line + "\n" +
      failStatement + "\n" +
      "}\n" +
      "catch [Exception] {}\n";
}


